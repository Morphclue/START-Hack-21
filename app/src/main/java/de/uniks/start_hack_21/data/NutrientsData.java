package de.uniks.start_hack_21.data;

public class NutrientsData {
    private String date;
    private int calories;
    private int fat;
    private int carbs;
    private int proteins;
    private int water;
    private int alcohol;
    private int caffeine;
    private int nicotine;

    public String getDate() {
        return date;
    }

    public int getCalories() {
        return calories;
    }

    public int getFat() {
        return fat;
    }

    public int getCarbs() {
        return carbs;
    }

    public int getProteins() {
        return proteins;
    }

    public int getWater() {
        return water;
    }

    public int getAlcohol() {
        return alcohol;
    }

    public int getCaffeine() {
        return caffeine;
    }

    public int getNicotine() {
        return nicotine;
    }
}
