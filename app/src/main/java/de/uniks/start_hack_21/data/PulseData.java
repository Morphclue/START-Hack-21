package de.uniks.start_hack_21.data;

public class PulseData {
    private String Date;
    private int pulseAverage;
    private int percentLow;
    private int percentMid;
    private int percentHigh;
    private int max;
    private int min;

    public String getDate() {
        return Date;
    }

    public int getPulseAverage() {
        return pulseAverage;
    }

    public int getPercentLow() {
        return percentLow;
    }

    public int getPercentMid() {
        return percentMid;
    }

    public int getPercentHigh() {
        return percentHigh;
    }

    public int getMax() {
        return max;
    }

    public int getMin() {
        return min;
    }
}
