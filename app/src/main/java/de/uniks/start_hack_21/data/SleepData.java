package de.uniks.start_hack_21.data;

public class SleepData {
    private String date;
    private int minutes;
    private int deepSleep;
    private int lightSleep;
    private int turns;
    private int awake;

    public String getDate() {
        return date;
    }

    public int getMinutes() {
        return minutes;
    }

    public int getDeepSleep() {
        return deepSleep;
    }

    public int getLightSleep() {
        return lightSleep;
    }

    public int getTurns() {
        return turns;
    }

    public int getAwake() {
        return awake;
    }
}
